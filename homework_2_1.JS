
// оператор + у виразі, де перший з операндів є рядком, перетворює всі операнди на рядки
'number' + 23 + 32 //'number2332' 


//т.я. на початку числа оператор + їх сумує, потім перетворює на рядок, тому що слово не переводиться в числове значення
41 + 1 + 'number' //'42number'

//якщо операнд не є числом, оператор + перетворить його на число, при цьому null стає 0
null + 1 // 1

//оператор + слова залишає рядками, арифметичний оператор + при використанні з рядком видає NaN і перетворює на рядок
'five' + + 'two'//'fiveNaN'

//&& повертає останнє значення, якщо при перетворенні всі дали true, а числа перетворюються в булеве значення true
2 && 7// 7

//унарний оператор + претворює рядок на число, потім аріфметичний + додає числа
+'40' + +'2';//42

//=== оператор строгої рівності, ліва частина: 10 стає числом, віднімається 5; 5 не дорівнює 6
'10' - 5 === 6;// false

//унарний плюс перетворює на число, а булеві значення перетворюються на 0(false) та 1(true) і сумуються
true + false//1

//т.я. при перетворенні рядка на число виникає помилка на літерах, отримуємо NaN
'4px' - 3//NaN

//рядок перетворюється на число без помилки, і проходить операція віднімання
'4' - 3// 1

//3 возводится в степень, т.я. множення в приорітеті, потім перетворюється на рядок і вираз конкатенується
'2' + 3 ** 2;//29

//перетворюється на число і ділиться
12 / '6'//2

//т.я. на початку числа оператор + їх сумує, потім перетворює на рядок, тому що слово не переводиться в числове значення
23 + 42 + 'number'// '65number'

//спочатку викорується те, що в дужках, т.я. 5 не дорівнює 6, отримуємо false, вираз конкатенується в рядок
'10' + (5 === 6);//'10false'

//оператор + у виразі, де перший з операндів є рядком, перетворює всі операнди на рядки
'number' + 15 + 3 // 'number153'